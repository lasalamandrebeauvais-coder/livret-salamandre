// Fonction pour appliquer les traductions au DOM
function applyTranslations(language = 'fr') {
    if (!translations[language]) {
        console.warn(`Langue '${language}' non found. Utilisation du français.`);
        language = 'fr';
    }
    
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[language][key]) {
            // Préserver le HTML si l'élément contient des enfants
            if (element.children.length === 0) {
                element.textContent = translations[language][key];
            } else {
                // Pour les éléments avec enfants (ex: boutons avec icônes), on cherche le texte
                let textNode = null;
                for (let child of element.childNodes) {
                    if (child.nodeType === 3) { // 3 = TEXT_NODE
                        textNode = child;
                        break;
                    }
                }
                if (textNode) {
                    textNode.textContent = translations[language][key];
                }
            }
        }
    });
}
